module.exports = function Product(prod) {
	this.name = prod.name;
	this.id = prod.id;
	this.image = prod.image;
	this.price = prod.price;
	this.view = prod.view;
	this.buy = prod.buy;
	this.description = prod.description;
	this.origin = prod.origin;
	this.type = prod.type;
	this.manufacturer = prod.manufacturer;
	this.date = prod.date;
}